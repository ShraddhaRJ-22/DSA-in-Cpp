// Size of Data Types

#include<iostream>
using namespace std;
int main()
{
	int a; // Integer
	cout<<"size of int:"<<sizeof(a)<<endl;
	
	float b; // Float
	cout<<"size of float:"<<sizeof(b)<<endl;
	
	char c; // Character
	cout<<"size of char:"<<sizeof(c)<<endl;
	
	bool d; // Boolean
	cout<<"size of bool:"<<sizeof(d)<<endl;
	
	return 0;
}
